#!/usr/bin/env node

import { mkdirSync, readdirSync, writeFileSync } from "node:fs";
import path from "node:path";

const workspaceRoot = process.cwd();
const functionsDir = path.join(workspaceRoot, "functions");
const outputFile = path.join(functionsDir, "_worker", "generated-routes.ts");

function walk(dir) {
  const out = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      out.push(...walk(fullPath));
      continue;
    }
    if (!entry.isFile()) continue;
    if (!fullPath.endsWith(".ts")) continue;
    out.push(fullPath);
  }
  return out;
}

function toPosix(p) {
  return p.split(path.sep).join("/");
}

function routeFromFile(relPath) {
  const clean = relPath.replace(/\.ts$/, "");
  const segments = clean.split("/").filter(Boolean);
  if (segments[segments.length - 1] === "index") {
    segments.pop();
  }
  const routeSegments = segments.map((segment) => {
    const dynamic = segment.match(/^\[(.+)\]$/);
    return dynamic ? `:${dynamic[1]}` : segment;
  });
  const routePath = `/${routeSegments.join("/")}`.replace(/\/+/g, "/") || "/";
  return routePath;
}

function scoreRoute(routePath) {
  const segments = routePath.split("/").filter(Boolean);
  const dynamicCount = segments.filter((segment) => segment.startsWith(":"))
    .length;
  return {
    dynamicCount,
    segmentCount: segments.length,
    staticCount: segments.length - dynamicCount,
    length: routePath.length,
  };
}

const allFiles = walk(functionsDir)
  .map((absPath) => toPosix(path.relative(functionsDir, absPath)))
  .filter((relPath) => !relPath.startsWith("_lib/"))
  .filter((relPath) => !relPath.startsWith("_worker/"))
  .filter((relPath) => !relPath.endsWith(".d.ts"))
  .filter((relPath) => !relPath.endsWith("/_middleware.ts"));

const byRoute = new Map();
for (const relPath of allFiles) {
  const routePath = routeFromFile(relPath);
  const list = byRoute.get(routePath) ?? [];
  list.push(relPath);
  byRoute.set(routePath, list);
}

const routes = [];
for (const [routePath, relPaths] of byRoute.entries()) {
  const preferred =
    relPaths.find((entry) => entry.endsWith("/index.ts")) ?? relPaths[0];
  routes.push({
    routePath,
    source: preferred,
  });
}

routes.sort((a, b) => {
  const sa = scoreRoute(a.routePath);
  const sb = scoreRoute(b.routePath);

  if (sa.dynamicCount !== sb.dynamicCount) {
    return sa.dynamicCount - sb.dynamicCount;
  }
  if (sa.staticCount !== sb.staticCount) {
    return sb.staticCount - sa.staticCount;
  }
  if (sa.segmentCount !== sb.segmentCount) {
    return sb.segmentCount - sa.segmentCount;
  }
  if (sa.length !== sb.length) {
    return sb.length - sa.length;
  }
  return a.routePath.localeCompare(b.routePath);
});

const imports = [];
const manifestRows = [];

routes.forEach((route, index) => {
  const importName = `routeModule${index + 1}`;
  const importPath = `../${route.source.replace(/\.ts$/, "")}`;
  imports.push(`import * as ${importName} from "${importPath}";`);
  manifestRows.push(
    `  { path: ${JSON.stringify(route.routePath)}, source: ${JSON.stringify(route.source)}, module: ${importName} },`,
  );
});

const duplicates = [];
for (const [routePath, relPaths] of byRoute.entries()) {
  if (relPaths.length <= 1) continue;
  duplicates.push(`- ${routePath} => ${relPaths.join(", ")}`);
}

const duplicateComment = duplicates.length
  ? `\n// Duplicate route candidates (index.ts preferred):\n${duplicates
      .map((line) => `// ${line}`)
      .join("\n")}\n`
  : "\n";

const output = `/* eslint-disable */\n// This file is generated by scripts/generate-worker-routes.mjs.\n// Do not edit by hand.\n\nimport type { RouteModule } from "./types";\n\n${imports.join("\n")}\n\nexport interface GeneratedRoute {\n  path: string;\n  source: string;\n  module: RouteModule;\n}\n${duplicateComment}\nexport const generatedRoutes: GeneratedRoute[] = [\n${manifestRows.join("\n")}\n];\n`;

mkdirSync(path.dirname(outputFile), { recursive: true });
writeFileSync(outputFile, output, "utf8");
console.log(`Generated ${routes.length} routes in ${toPosix(path.relative(workspaceRoot, outputFile))}`);
